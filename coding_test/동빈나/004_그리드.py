# 거스름돈
# N원의 거스름돈을 500원, 100원, 50원, 10원으로 거슬러주기
# 최소의 동전만 사용하자
# 그러면 그냥 큰거부터 쭈루룩하면 돼(그리드)

# 그렇게 해도 되는 이유
# 가지고 있는 동전 중에서 큰단위가 항상 작은 단위의 배수이므로
# 작은 단위의 동전들을 종합해 다른 해가 나올 수 없기 때문이다.

n= int(input())
count = 0
array = [500, 100, 50, 10]
for coin in array:
    count += n//coin
    n = n%coin
print(count)